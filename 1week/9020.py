def sieve_of_eratosthenes(n):
    primes = [True] * (n + 1)
    primes[0] = primes[1] = False
    for i in range(2, int(n**0.5) + 1):
        if primes[i]:
            for j in range(i*i, n+1, i):
                primes[j] = False
    return primes

MAX = 10000
is_prime = sieve_of_eratosthenes(MAX)

T = int(input())
for _ in range(T):
    n = int(input())
    for a in range(n//2, 1, -1):
        if is_prime[a] and is_prime[n-a]:
            print(a, n-a)
            break



